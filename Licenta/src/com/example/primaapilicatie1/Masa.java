package com.example.primaapilicatie1;

public class Masa {
	private String nume;
	private String parola;

	public Masa(String nume, String parola) {
		this.nume = nume;
		this.parola = parola;
	}

	public String getNume() {
		return nume;
	}

	public String getParola() {
		return parola;
	}

}
